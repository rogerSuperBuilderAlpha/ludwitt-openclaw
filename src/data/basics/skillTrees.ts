/**
 * Skill Mastery Trees Data
 * Contains all skill tree definitions for different learning paths
 */

export interface SkillNode {
  id: string
  name: string
  description: string
  icon: string
  category: 'math' | 'reading' | 'latin' | 'greek' | 'logic' | 'mixed'
  difficulty: number // 1-12 (grade level)
  prerequisites: string[] // IDs of required skills
  xpReward: number
  estimatedTime: number // minutes
  unlocked: boolean
  completed: boolean
  inProgress: boolean
  position: { x: number; y: number } // Position in the tree
  connections: string[] // IDs of skills this connects to
}

export interface SkillTree {
  id: string
  name: string
  description: string
  skills: SkillNode[]
  totalSkills: number
  completedSkills: number
}

