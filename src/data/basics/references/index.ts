// Get all reference units for a section
export function getReferenceUnits(sectionId: string): ReferenceUnit[] {
  return REFERENCE_CONTENT[sectionId] || []
}

// Get a specific unit by ID
export function getReferenceUnit(sectionId: string, unitId: string): ReferenceUnit | undefined {
  const units = REFERENCE_CONTENT[sectionId] || []
  return units.find(u => u.id === unitId)
}

