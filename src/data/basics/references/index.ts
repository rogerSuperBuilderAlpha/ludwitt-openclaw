// Find relevant units for a given topic
export function getUnitsForTopic(topic: string): string[] {
  // Direct match
  if (TOPIC_TO_UNIT_MAP[topic]) {
    return TOPIC_TO_UNIT_MAP[topic]
  }
  
  // Fuzzy match - check if topic contains any of the keys
  for (const [key, units] of Object.entries(TOPIC_TO_UNIT_MAP)) {
    if (topic.toLowerCase().includes(key.toLowerCase()) ||
        key.toLowerCase().includes(topic.toLowerCase())) {
      return units
    }
  }
  
  return []
}

// Get the section ID from a problem type
export function getSectionFromProblemType(type: string): string {
  const typeMap: Record<string, string> = {
    'arithmetic': 'arithmetic',
    'pre-algebra': 'pre-algebra',
    'algebra': 'algebra',
    'geometry': 'geometry',
    'trigonometry': 'trigonometry',
    'precalculus': 'precalculus',
    'calculus': 'calculus',
    'linear-algebra': 'linear-algebra',
    'discrete-math': 'discrete-math',
    'statistics': 'statistics',
    'word-problem': 'word-problems', // Note: REFERENCE_CONTENT uses 'word-problems'
    'word-problems': 'word-problems',
    'latin': 'latin',
    'greek': 'greek',
  }
  return typeMap[type] || 'algebra'
}

